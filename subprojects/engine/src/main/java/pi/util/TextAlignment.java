package pi.util;

/**
 * Die <b>Textausrichtung</b> wie Buchstaben in einer Textzeile oder einem
 * Textfeld ausgerichtet werden können.
 */
public enum TextAlignment
{
    /**
     * Linksbündig.
     */
    LEFT,
    /**
     * Zentriert.
     */
    CENTER,
    /**
     * Rechtsbündig.
     */
    RIGHT
}
